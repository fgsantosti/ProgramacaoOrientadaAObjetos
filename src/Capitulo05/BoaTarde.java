public class BoaTarde {
    public static void main(String[] args) {
        
        for (int i = 1; i <= 23; i++) {
            if (i%2==0) {
                System.out.println("Boa Tarde pessoal do numero par "+i);
            }else{
                System.out.println("Boa Tarde pessoal do numero impar "+i);
            }
            
        }
                
    }   
            
}
